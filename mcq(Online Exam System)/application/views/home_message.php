    ২. শিক্ষকের অনুমোদিত বিষয় সিলেক্ট করে পরীক্ষা দিতে হবে।<br>
    ৩. শিক্ষার্থী একই বিষয়ে একের অধিক বার পরীক্ষায় অংশগ্রহন করতে পারবেনা।<br>
